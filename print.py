# print displays output to your console
print('Hello World!')   # Basic print statement

# Enclose strings in single or double quotes
print('Hello World single quotes')
print("Hello World double quotes")

# Getting information from the user
name = input('Please enter your name: ')   # Stores user input in variable 'name'
print(name)  # Prints whatever the user typed

# Printing blank lines can improve readability
print('Hello World')
print()   # Prints a blank line
print('Did you see that blank line ?')

# Using \n to insert a line break inside a string
print('Blank line \nin the middle of string')